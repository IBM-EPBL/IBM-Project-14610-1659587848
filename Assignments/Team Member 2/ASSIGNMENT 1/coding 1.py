

a=[1,2,3,4,5]
print(a)

#insertion
a.insert(5,10)
print(a)

#deletion of first occurence of integer
del a[0]
print(a)

#insertion at end of a
a.append(-5)
print(a)

#sorting
a.sort()
print(a)

#popping last element
a.pop()
print(a)

#reverse the list
a.reverse()
print(a)

